package basic.operator;

public class UnaryOperator {
    public static void main(String[] args) {

        int i = 1;
        int j = i++;
        int k = j--;

        // 1
        // 2
        // 2


        System.out.println("i = " + i);
        System.out.println("j = " + j);
        System.out.println("k = " + k);

        //증감 연산자 (++ , --)
        // 변수의 값을 단순히 하나 올리거나 내릴때 사용합니다.


    }
}
